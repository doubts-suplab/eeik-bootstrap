# ── main ──────────────────────────────────────────────────────────────────────

ALL_TOOLS = ["kiro", "codex", "cursor", "gemini"]

GENERATORS = {
    "kiro":   generate_kiro,
    "codex":  generate_codex,
    "cursor": generate_cursor,
    "gemini": generate_gemini,
}


def main() -> int:
    parser = argparse.ArgumentParser(description="EEIK Adapter Generator")
    parser.add_argument("--apply",  action="store_true", help="Write files (default: dry-run)")
    parser.add_argument("--tools",  default=",".join(ALL_TOOLS),
                        help=f"Comma-separated tools to generate (default: all). Options: {','.join(ALL_TOOLS)}")
    parser.add_argument("--list",   action="store_true", help="List adapters and exit")
    args = parser.parse_args()

    dry   = not args.apply
    tools = [t.strip() for t in args.tools.split(",") if t.strip()]

    print(f"\n{ANSI_BOLD}EEIK Adapter Generator{ANSI_RESET}")
    print(f"  Mode  : {'dry-run (pass --apply to write)' if dry else 'APPLY'}")
    print(f"  Tools : {', '.join(tools)}\n")

    if args.list:
        for t in ALL_TOOLS:
            print(f"  {'✓' if t in tools else '—'} {t}")
        return 0

    manifest = load_manifest()
    if not manifest:
        print(f"{ANSI_YELLOW}⚠ No project-manifest.yaml found — generating with generic defaults{ANSI_RESET}")

    for tool in tools:
        if tool not in GENERATORS:
            print(f"\n{ANSI_YELLOW}⚠ Unknown tool '{tool}' — skipping{ANSI_RESET}")
            continue
        GENERATORS[tool](manifest, dry)

    print(f"\n{ANSI_BOLD}Done.{ANSI_RESET}")
    if dry:
        print(f"  Pass --apply to write files.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
