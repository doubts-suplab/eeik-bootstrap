# ── Cursor ────────────────────────────────────────────────────────────────────

def generate_cursor(manifest: dict, dry: bool) -> None:
    print(f"\n{ANSI_BOLD}Cursor (.cursor/rules/){ANSI_RESET}")
    golden = read_golden_rules()

    # golden-rules.mdc
    golden_mdc = f"""---
description: EEIK Golden Rules — apply to ALL code in ALL files
globs: ["**/*.java", "**/*.ts", "**/*.py", "**/*.kt"]
alwaysApply: true
---
{GENERATED_BANNER}
{golden}

## Enforcement

These rules are BLOCKERS in code review. No exceptions.
Full standards: `capability-packs/core/standards/golden-rules.md`
"""
    write_file(REPO_ROOT / ".cursor" / "rules" / "golden-rules.mdc", golden_mdc, dry)

    # architecture.mdc
    arch_mdc = f"""---
description: EEIK Architecture Rules — hexagonal architecture and DDD
globs: ["**/*.java", "**/*.ts"]
alwaysApply: true
---
{GENERATED_BANNER}
## Hexagonal Architecture

Package structure (Java):
```
domain/         — Entities, value objects, domain events, ports (interfaces)
application/    — Use cases, command/query handlers
infrastructure/ — JPA repos, messaging, external clients
web/            — REST controllers, DTOs
```

Dependencies flow INWARD only:
- infrastructure → application → domain
- web → application
- domain has ZERO dependencies on other layers

## DDD Rules

- Aggregates enforce invariants — no direct field access from outside
- Domain events for cross-context communication — never direct DB joins across contexts
- Value objects are immutable
- Repositories return domain objects, not JPA entities

## Knowledge Base

- Patterns: `knowledge/patterns/`
- Anti-patterns: `knowledge/anti-patterns/`
- Reference architectures: `knowledge/reference-architectures/`
"""
    write_file(REPO_ROOT / ".cursor" / "rules" / "architecture.mdc", arch_mdc, dry)

    # security.mdc
    security_mdc = f"""---
description: EEIK Security Rules — apply to all files
globs: ["**/*"]
alwaysApply: true
---
{GENERATED_BANNER}
## Security Non-Negotiables

- NEVER hardcode credentials, API keys, passwords, or AWS account IDs
- ALL secrets go to AWS Secrets Manager / environment variables
- NEVER log sensitive data (PII, tokens, passwords)
- SQL must use parameterised queries only — never string concatenation
- Input validation on ALL public API endpoints
- Dependency versions must be explicit — no `latest` or `*`

## OWASP Top 10 Checklist

Before any PR touching auth, data access, or external input:
- [ ] Injection — parameterised queries, no string-built SQL
- [ ] Broken Auth — JWT validation, session management
- [ ] Sensitive Data Exposure — encryption at rest and in transit
- [ ] Security Misconfiguration — no default passwords, no stack traces in responses
- [ ] XSS — input sanitisation for any HTML output

Full standard: `capability-packs/core/standards/security-baseline.md`
"""
    write_file(REPO_ROOT / ".cursor" / "rules" / "security.mdc", security_mdc, dry)


