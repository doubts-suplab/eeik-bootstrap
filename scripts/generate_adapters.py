#!/usr/bin/env python3
"""
EEIK Adapter Generator
Generates AI tool adapter files from the shared capability-packs intelligence.

Adapters produced:
  .kiro/steering/    — Kiro (AWS IDE)
  .kiro/hooks/       — Kiro hooks
  AGENTS.md          — OpenAI Codex CLI
  .cursor/rules/     — Cursor IDE
  GEMINI.md          — Google Gemini CLI

Usage:
    python3 scripts/generate_adapters.py                     # dry-run
    python3 scripts/generate_adapters.py --apply             # write files
    python3 scripts/generate_adapters.py --apply --tools kiro,codex
    python3 scripts/generate_adapters.py --list              # show what would be generated
"""

import sys
import argparse
from pathlib import Path
from datetime import datetime

try:
    import yaml
except ImportError:
    print("ERROR: pyyaml required.  Run: pip install pyyaml", file=sys.stderr)
    sys.exit(1)

REPO_ROOT  = Path(__file__).parent.parent
PACKS_DIR  = REPO_ROOT / "capability-packs"

ANSI_BOLD   = "\033[1m"
ANSI_GREEN  = "\033[92m"
ANSI_YELLOW = "\033[93m"
ANSI_CYAN   = "\033[96m"
ANSI_RESET  = "\033[0m"

GENERATED_BANNER = f"<!-- Generated by EEIK generate_adapters.py on {datetime.utcnow().strftime('%Y-%m-%d')} — do not edit by hand -->\n"
GENERATED_BANNER_YAML = f"# Generated by EEIK generate_adapters.py on {datetime.utcnow().strftime('%Y-%m-%d')} — do not edit by hand\n"


def load_manifest() -> dict:
    candidates = [
        REPO_ROOT / "project-manifest.yaml",
        REPO_ROOT / "bootstrap" / "manifests" / "project-manifest.yaml",
    ]
    for p in candidates:
        if p.exists():
            with open(p) as f:
                return yaml.safe_load(f) or {}
    return {}


def write_file(path: Path, content: str, dry: bool) -> None:
    if dry:
        print(f"  {ANSI_CYAN}Would write{ANSI_RESET}  {path.relative_to(REPO_ROOT)}  ({len(content)} chars)")
    else:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        print(f"  {ANSI_GREEN}Written{ANSI_RESET}     {path.relative_to(REPO_ROOT)}")


def read_golden_rules() -> str:
    """Load golden rules from core standards."""
    rules_file = PACKS_DIR / "core" / "standards" / "golden-rules.md"
    if rules_file.exists():
        return rules_file.read_text(encoding="utf-8")
    # Fallback condensed version
    return """## Golden Rules
1. Constructor injection only — no @Autowired on fields
2. No hardcoded secrets — use Secrets Manager or env vars
3. SLF4J not System.out — parameterised log messages
4. SOLID principles — Single Responsibility, Open/Closed, Liskov, ISP, DIP
5. Domain-Driven Design — respect bounded context boundaries
6. No SELECT * — always specify explicit column lists
7. Parameterised queries only — never build SQL via string concatenation
8. Conventional Commits — type(scope): description
9. No partial implementations — every method body is complete
10. jakarta.* in Spring Boot 3.x — never javax.*
11. No Thread.sleep() in tests — use Awaitility
12. No empty catch blocks — log at WARN/ERROR minimum
"""


def read_tech_summary(manifest: dict) -> str:
    tech = manifest.get("technology", {})
    lines = []
    b = tech.get("backend", {})
    if b.get("language"):
        lines.append(f"- Backend: {b['language']} {b.get('version','')} / {b.get('framework','')}")
    f = tech.get("frontend", {})
    if f.get("framework"):
        lines.append(f"- Frontend: {f['framework']} {f.get('version','')}")
    c = tech.get("cloud", {})
    if c.get("provider", "none") != "none":
        lines.append(f"- Cloud: {c['provider']} / IaC: {c.get('iac_tool','none')}")
    d = tech.get("database", {})
    if d.get("primary", "none") != "none":
        lines.append(f"- Database: {d['primary']}")
    return "\n".join(lines) if lines else "- (no manifest — generic enterprise defaults apply)"


# ── Kiro ──────────────────────────────────────────────────────────────────────

def generate_kiro(manifest: dict, dry: bool) -> None:
    print(f"\n{ANSI_BOLD}Kiro (.kiro/){ANSI_RESET}")
    project = manifest.get("project", {})
    name    = project.get("name", "this project")
    tech_summary = read_tech_summary(manifest)
    golden       = read_golden_rules()

    # steering/product.md
    product_md = f"""{GENERATED_BANNER}# Product Steering — {name}

> Kiro injects this file into every interaction as persistent context.
> Keep it current. It is the source of truth for what this product is.

## What This Is

{project.get('description', f'Enterprise application: {name}')}

- **Project type**: {project.get('type', 'greenfield')}
- **Domain**: {project.get('domain', 'generic')}
- **Environments**: {', '.join(project.get('environments', ['dev', 'staging', 'prod']))}

## Team Context

This project uses the **EEIK bootstrap** configuration stack:
- Claude Code agents in `.claude/agents/`
- Capability packs in `capability-packs/`
- Shared knowledge in `knowledge/`

## Business Rules

Consult `knowledge/business-rules/` for domain-specific rules.
Consult `.claude/memory/domain-glossary.md` for terminology.
"""

    # steering/tech.md
    tech_md = f"""{GENERATED_BANNER}# Technology Steering — {name}

> Always-on technology context for Kiro. Keep aligned with project-manifest.yaml.

## Stack

{tech_summary}

## Governance

- Profile: {manifest.get('governance', {}).get('profile', 'standard')}
- Branch strategy: {manifest.get('governance', {}).get('branch_strategy', 'github-flow')}
- Coverage threshold: {manifest.get('governance', {}).get('coverage_threshold', 80)}%

## Non-Negotiable Rules

{golden}

## Standards Files

Full standards are in `capability-packs/core/standards/` and `.claude/standards/`.
"""

    # steering/structure.md
    structure_md = f"""{GENERATED_BANNER}# Structure Steering — {name}

> Repository layout and architecture patterns. Kiro reads this on every task.

## Repository Layout

```
.claude/          Claude Code agents, commands, hooks, memory
.kiro/            Kiro steering and hooks (this directory)
.github/          Copilot instructions + CI/CD workflows
capability-packs/ Intelligence packs (agents, standards, workflows)
knowledge/        ADRs, patterns, anti-patterns, incidents
generators/       Manifest-driven generators
scripts/          CLI tools (validate, activate, generate-adapters)
templates/        Code templates per technology
```

## Architecture Pattern

- **Hexagonal Architecture** — domain / application / infrastructure / web layers
- **Domain-Driven Design** — bounded contexts, aggregates, domain events
- Dependencies always flow inward (domain has zero outward dependencies)

## Package Structure (Java)

```
com.{name.lower().replace('-','.')}/
  domain/         Entities, value objects, domain events, ports
  application/    Use cases, command/query handlers
  infrastructure/ JPA repos, messaging adapters, external clients
  web/            REST controllers, request/response DTOs
```

## Before Writing Code

1. Identify bounded context and layer
2. Check `knowledge/patterns/` for existing patterns
3. Check `knowledge/anti-patterns/` for what NOT to do
4. Follow the Golden Rules in `.kiro/steering/tech.md`
"""

    write_file(REPO_ROOT / ".kiro" / "steering" / "product.md",   product_md,   dry)
    write_file(REPO_ROOT / ".kiro" / "steering" / "tech.md",      tech_md,      dry)
    write_file(REPO_ROOT / ".kiro" / "steering" / "structure.md", structure_md, dry)

    # hooks/format-on-save.json
    hooks_json = """{
  "hooks": [
    {
      "name": "post-edit-quality-check",
      "event": "file:saved",
      "matchPattern": "**/*.java",
      "command": "bash .claude/hooks/post-edit-check.sh",
      "description": "Run EEIK quality checks after Java file save"
    },
    {
      "name": "post-edit-ts-check",
      "event": "file:saved",
      "matchPattern": "**/*.ts",
      "command": "bash .claude/hooks/post-edit-check.sh",
      "description": "Run EEIK quality checks after TypeScript file save"
    }
  ]
}
"""
    write_file(REPO_ROOT / ".kiro" / "hooks" / "quality-hooks.json", hooks_json, dry)

    # .kiro/settings.json
    settings_json = f"""{GENERATED_BANNER_YAML.replace('# Generated', '// Generated')}{{
  "model": "claude-sonnet-4-6",
  "steeringFiles": [
    ".kiro/steering/product.md",
    ".kiro/steering/tech.md",
    ".kiro/steering/structure.md"
  ],
  "specsDirectory": ".kiro/specs",
  "hooksDirectory": ".kiro/hooks"
}}
"""
    write_file(REPO_ROOT / ".kiro" / "settings.json", settings_json, dry)


